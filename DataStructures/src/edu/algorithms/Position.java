package edu.algorithms;

public class Position {
	private int x; // column
	private int y; // row
	
	public Position(int y, int x) {
		this.setX(x);
		this.setY(y);
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
